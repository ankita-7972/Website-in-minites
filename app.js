var dest=new Date ("sep.19,2024.10:00:00").getTime();

var x=setInterval(function(){
    var now=new Date().getTime();
    var diff=dest-now;
    var days=Math.floor(diff/(1000*60*60*24));
    console.log(days);
    var hours=Math.floor((diff%(1000*60*60))/(1000*60*60));
    console.log(hours);
    var minutes=Math.floor((diff%(1000*60*60))/(1000*60));
    console.log(minutes);
    var seconds=Math.floor((diff%(1000*60))/1000);
    console.log(seconds);

    document.getElementById("Tday").innerHTML=days
    document.getElementById("Tday1").innerHTML=hours
    document.getElementById("Tday2").innerHTML=minutes
    document.getElementById("Tday3").innerHTML=seconds
},1000);
